    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String Id=jTextField1.getText();
        String name=jTextField2.getText();
        String number=jTextField3.getText();
        String birth=jTextField4.getText();
        String gender=(String)jComboBox1.getSelectedItem();
        String add=jTextField5.getText();
        String dis=jTextField6.getText();
        try{
            Connection con=Connection.getCon();
            Statement st=con.createStatement();
        
           st.executeUpdate("Insert into Patients values('"+Id+"','"+name+"','"+number+"','"+birth+"','"+gender+"','"+add+"','"+dis+"')");
JOptionPane.showMessageDialog(null,"Patient Updated");
           setVisible(false);
           new AddPatient().setVisible(true);
        }
         catch(Exception e)  {
           JOptionPane.showMessageDialog(null,"Enter Correct Information"); 
            
        }
     
    }//GEN-LAST:event_jButton5ActionPerformed

